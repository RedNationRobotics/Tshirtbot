package org.usfirst.frc.team4576.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {

	// Talon IDs:
	public static final int LEFT_MASTER = 0;
	public static final int RIGHT_MASTER = 1;
	public static final int LEFT_SLAVE = 2;
	public static final int RIGHT_SLAVE = 3;

	// Solenoid IDs:
	public static final int SOLENOID_0 = 0;
	public static final int SOLENOID_1 = 1;
	public static final int SOLENOID_2 = 2;
	public static final int SOLENOID_SV0 = 7;
	public static final int SOLENOID_SV1 = 6;
	public static final int SOLENOID_SV2 = 5;


	
}
